// Task 2
fun dogToHumanYears(dogAge: Double): Double {
    return if (dogAge <= 2) {
        dogAge * 10.5
    } else {
        2 * 10.5 + (dogAge - 2) * 4
    }
}


//Task 3
fun chooseTransport(distance: Double): String {
    return when {
        distance <= 1 -> "пешком"
        distance <= 5 -> "велосипед"
        else -> "автотранспорт"
    }
}

//Task 4
fun calculateBonus(purchaseAmount: Double): Int {
    return if (purchaseAmount <= 1000) {
        (purchaseAmount / 100 * 2).toInt()
    } else {
        (purchaseAmount / 100 * 5).toInt()
    }
}

//Task 5
fun getFileType(extension: String): String {
    return when (extension.lowercase()) {
        "txt", "doc", "docx" -> "Текстовый документ"
        "jpg", "jpeg", "png", "gif" -> "Изображение"
        "xls", "xlsx" -> "Таблица"
        else -> "Неизвестный тип"
    }
}

//Task 6
fun convertTemperature(value: Double, unit: String): String {
    return if (unit.uppercase() == "C") {
        val result = (value * 9/5) + 32
        "$result F"
    } else if (unit.uppercase() == "F") {
        val result = (value - 32) * 5/9
        "$result C"
    } else {
        "Некорректная единица измерения"
    }
}

//Task 7
fun recommendClothing(temperature: Double) : String {
    return when {
        temperature < -30 -> "Не выходите из дома"
        temperature < 0 -> "Куртка и шапка"
        temperature <= 15 -> "Ветровка"
        temperature > 35 -> "Не выходите из дома, слишком жарко!"
        else -> "Футболка и шорты"
    }
}


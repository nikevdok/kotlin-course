fun getAvailableCategories(age: Int): String {
    return when {
        age < 13 -> "Доступные категории: детские"
        age in 13..17 -> "Доступные категории: детские, подростковые"
        age >= 18 -> "Доступные категории: детские, подростковые, 18+"
        else -> "Некорректный возраст"
    }
}